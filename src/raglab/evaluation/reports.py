"""Leaderboard reports: CSV + standalone HTML, ranked by a chosen metric."""

from __future__ import annotations

import csv
import html
from pathlib import Path
from typing import Any


def _columns(rows: list[dict[str, Any]]) -> list[str]:
    cols: list[str] = []
    for row in rows:
        for k in row:
            if k not in cols:
                cols.append(k)
    return cols


def write_csv(rows: list[dict[str, Any]], path: str | Path) -> Path:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    cols = _columns(rows)
    with path.open("w", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=cols)
        writer.writeheader()
        for row in rows:
            writer.writerow({c: row.get(c, "") for c in cols})
    return path


def write_html(
    rows: list[dict[str, Any]], path: str | Path, title: str = "RAGLab Leaderboard"
) -> Path:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    cols = _columns(rows)
    head = "".join(f"<th>{html.escape(c)}</th>" for c in cols)
    body = "".join(
        "<tr>"
        + "".join(f"<td>{html.escape(str(row.get(c, '')))}</td>" for c in cols)
        + "</tr>"
        for row in rows
    )
    doc = f"""<!doctype html><html><head><meta charset="utf-8">
<title>{html.escape(title)}</title>
<style>
 body{{font-family:system-ui,sans-serif;margin:2rem;background:#0b0e14;color:#e6e6e6}}
 h1{{font-weight:600}}
 table{{border-collapse:collapse;width:100%;font-size:14px}}
 th,td{{border:1px solid #2a2f3a;padding:6px 10px;text-align:left}}
 th{{background:#161b26;position:sticky;top:0}}
 tr:nth-child(even){{background:#11151d}}
 caption{{caption-side:bottom;padding-top:10px;color:#8a93a6;font-size:12px}}
</style></head><body>
<h1>{html.escape(title)}</h1>
<table><thead><tr>{head}</tr></thead><tbody>{body}</tbody>
<caption>{len(rows)} experiments — generated by RAGLab</caption></table>
</body></html>"""
    path.write_text(doc, encoding="utf-8")
    return path
